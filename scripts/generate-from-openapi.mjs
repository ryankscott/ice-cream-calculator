function resolveSchema(schemas, schemaName) {
  const seen = new Set()

  function inner(schema) {
    if (schema?.$ref) {
      const refName = schema.$ref.replace('#/components/schemas/', '')
      if (seen.has(refName)) {
        throw new Error(`Circular $ref detected for ${refName}`)
      }
      seen.add(refName)
      return inner(schemas[refName])
    }

    const combinedProps = {}
    const combinedRequired = new Set()

    if (Array.isArray(schema?.allOf)) {
      for (const part of schema.allOf) {
        const resolved = inner(part)
        Object.assign(combinedProps, resolved.properties)
        resolved.required.forEach((item) => combinedRequired.add(item))
      }
    }

    if (schema?.type === 'object' && schema?.properties) {
      Object.assign(combinedProps, schema.properties)
      if (Array.isArray(schema.required)) {
        schema.required.forEach((item) => combinedRequired.add(item))
      }
    }

    return {
      properties: combinedProps,
      required: combinedRequired,
      enum: schema?.enum,
    }
  }

  return inner({ $ref: `#/components/schemas/${schemaName}` })
}

function ensureProperties(schema, properties) {
  for (const property of properties) {
    if (!(property in schema.properties)) {
      throw new Error(
        `Expected property "${property}" missing from OpenAPI schema.`,
      )
    }
  }
}

