import { spawn } from 'node:child_process'
import path from 'node:path'
import { createRequire } from 'node:module'

import fs from 'fs-extra'
import openapiTS, { astToString } from 'openapi-typescript'
import YAML from 'yaml'

const ROOT_DIR = process.cwd()
const SPEC_PATH = path.resolve(ROOT_DIR, 'schema/ingredients.yaml')
const GENERATED_DIR = path.resolve(ROOT_DIR, 'src/server/generated')
const ENTITIES_DIR = path.resolve(ROOT_DIR, 'src/server/entities')
const MIGRATIONS_DIR = path.resolve(ROOT_DIR, 'src/server/migrations')
const requireModule = createRequire(import.meta.url)

async function main() {
  const rawSpec = await fs.readFile(SPEC_PATH, 'utf8')
  const spec = YAML.parse(rawSpec)

  await fs.ensureDir(GENERATED_DIR)
  await fs.ensureDir(ENTITIES_DIR)
  await fs.ensureDir(MIGRATIONS_DIR)

  await generateTypes(rawSpec)
  await generateZodSchemas()
  await generateTypeOrmArtifacts(spec)

  console.log('✔ Generated assets from OpenAPI spec.')
}

async function generateTypes(rawSpec) {
  const ast = await openapiTS(rawSpec, {
    alphabetize: true,
    immutable: false,
  })
  const output = astToString(ast)

  const fileContents = [
    '/* eslint-disable */',
    '// This file is automatically generated via scripts/generate-from-openapi.mjs',
    output,
  ].join('\n')

  await fs.writeFile(
    path.join(GENERATED_DIR, 'openapi-types.ts'),
    fileContents,
    'utf8',
  )
}

async function generateZodSchemas() {
  const pkgRoot = path.dirname(
    requireModule.resolve('openapi-zod-client/package.json'),
  )
  const executable = path.join(pkgRoot, 'bin.js')
  const fallbackExists = await fs.pathExists(executable)
  if (!fallbackExists) {
    throw new Error(
      'Unable to locate openapi-zod-client executable. Please run `pnpm install`.',
    )
  }

  const outputFile = path.join(GENERATED_DIR, 'zod-schemas.temp.ts')
  await fs.ensureDir(path.dirname(outputFile))

  await runNodeScript(executable, [
    SPEC_PATH,
    '--export-schemas',
    '--group-strategy',
    'none',
    '--output',
    outputFile,
    '--strict-objects',
    '--no-with-alias',
  ])

  const generated = await fs.readFile(outputFile, 'utf8')
  const withoutClient = generated.split('\nconst endpoints')[0] ?? generated
  const schemaOnly = withoutClient.replace(
    /^import { makeApi, Zodios, type ZodiosOptions } from "@zodios\/core";\n/,
    '',
  )

  const header = [
    '/* eslint-disable */',
    '// Generated Zod schemas derived from schema/ingredients.yaml',
  ].join('\n')

  await fs.writeFile(
    path.join(GENERATED_DIR, 'zod-schemas.ts'),
    `${header}\n${schemaOnly.trimEnd()}\n`,
    'utf8',
  )

  await fs.remove(outputFile)
}

function runNodeScript(executable, args) {
  return new Promise((resolve, reject) => {
    const child = spawn(process.execPath, [executable, ...args], {
      stdio: 'inherit',
    })

    child.on('error', reject)
    child.on('exit', (code) => {
      if (code === 0) {
        resolve()
      } else {
        reject(new Error(`Command failed with exit code ${code}`))
      }
    })
  })
}

async function generateTypeOrmArtifacts(spec) {
  const schemas = spec?.components?.schemas
  if (!schemas) {
    throw new Error('No schemas found inside OpenAPI components section.')
  }

  const supplierSchema = resolveSchema(schemas, 'Supplier')
  const ingredientSchema = resolveSchema(schemas, 'Ingredient')
  const ingredientStatus = resolveSchema(schemas, 'IngredientStatus')

  await fs.writeFile(
    path.join(ENTITIES_DIR, 'Supplier.ts'),
    buildSupplierEntity(supplierSchema),
    'utf8',
  )

  await fs.writeFile(
    path.join(ENTITIES_DIR, 'Ingredient.ts'),
    buildIngredientEntity(ingredientSchema, ingredientStatus),
    'utf8',
  )

  await fs.writeFile(
    path.join(
      MIGRATIONS_DIR,
      '1710000000000-CreateSuppliersAndIngredients.ts',
    ),
    buildMigration(),
    'utf8',
  )
}

